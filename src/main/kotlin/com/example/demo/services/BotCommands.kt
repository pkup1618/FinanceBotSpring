package com.example.demo.services

enum class BotCommands {
    START,
    HELP,
    LOCALE,
    SET_RU_LOCALE,
    SET_EN_LOCALE,
    MENU,
    MY_HABITS,
    ADD_HABIT,
    REMOVE_HABIT,
    EDIT_HABIT,
    STATS_TODAY,
    STATS_YESTERDAY,
    NOTIFICATIONS,
    NOTIFICATIONS_ENABLE,
    NOTIFICATIONS_DISABLE,
    UNKNOWN
} /*
    Commands for bot-father settings:

    start - начать
    help - помощь
    locale - настройки языка
    set_ru_locale - установить русский язык
    set_en_locale - установить английский язык
    menu - главное меню
    my_habits - мои привычки
    add_habit - добавить привычку
    remove_habit - удалить привычку
    edit_habit - изменить привычку
    stats_today - статистика за сегодня
    stats_yesterday - статистика за вчера
    notifications - настройки уведомлений
    notifications_enable - включить уведомления
    notifications_disable - выключить уведомления

 */

